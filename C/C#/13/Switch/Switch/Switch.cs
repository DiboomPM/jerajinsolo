using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Switch
{
    class Switch
    {
        static void Main(string[] args)
        {
            var number = 2;
            switch (number)
            {
                case 1:
                    Console.WriteLine("11111");
                    break;
                case 2:
                    Console.WriteLine("22222");
                    break;

            }
        }
    }
}
